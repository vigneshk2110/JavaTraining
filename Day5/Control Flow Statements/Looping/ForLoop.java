public class ForLoop{

    public static void main(String[] args) {

        /* 
        for (initialization; condition; increment/decrement) {
            // code to be executed repeatedly
        }
        */
        
        // Print numbers from 1 to 5
        for (int i = 5; i >0 ; i--) {
            System.out.println(i);
        }

    }
}
